import { NestFactory } from '@nestjs/core';
import { SwaggerModule, DocumentBuilder } from '@nestjs/swagger';
import { ValidationPipe } from '@nestjs/common';
import { AppModule } from './app.module';

async function bootstrap() {
  const app = await NestFactory.create(AppModule);

  // Configurar validaci贸n global
  app.useGlobalPipes(new ValidationPipe({
    whitelist: true,
    forbidNonWhitelisted: true,
    transform: true,
  }));

  // Configurar CORS
  app.enableCors();

  // Configurar Swagger
  const config = new DocumentBuilder()
    .setTitle('Ticut API')
    .setDescription('API para gesti贸n de barber铆as')
    .setVersion('1.0')
    .addTag('auth', 'Autenticaci贸n y autorizaci贸n')
    .addTag('users', 'Gesti贸n de usuarios')
    .addTag('barbers', 'Gesti贸n de barberos')
    .addTag('services', 'Gesti贸n de servicios')
    .addTag('clients', 'Gesti贸n de clientes')
    .addTag('dashboard', 'Dashboard y estad铆sticas')
    .addBearerAuth(
      {
        type: 'http',
        scheme: 'bearer',
        bearerFormat: 'JWT',
        name: 'JWT',
        description: 'Enter JWT token',
        in: 'header',
      },
      'JWT-auth',
    )
    .build();

  const document = SwaggerModule.createDocument(app, config);
  SwaggerModule.setup('api', app, document, {
    swaggerOptions: {
      persistAuthorization: true,
    },
  });

  await app.listen(4000);
  console.log(' Application is running on: http://localhost:4000');
  console.log(' Swagger documentation: http://localhost:4000/api');
}
bootstrap();
